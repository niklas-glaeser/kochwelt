function Calculate() {
    let amounts = [500, 1, 3, 1, 4, 1, 1, 200, 100, 1];
    let portion = +document.getElementById('inputField').value || 1;
  
    for (let i = 1; i <= amounts.length; i++) {
      let result = (portion * amounts[i - 1]) / 4;
      document.getElementById(`amount${i}`).innerHTML = `${result} `;
    }
}

